    /**
     *  @since  ver. 0.8.7 (edit. Chiffon)
     */
    public static function wpxHookType(string $hookname) {
        $type = null;
        foreach (self::$hooks as $hook_type => $names) {
            if (!$type) {
                if (in_array($hookname, $names)) {
                    $type = $hook_type;
                }
            }
        }
        return $type;
    }

